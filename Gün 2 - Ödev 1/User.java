public class User {
    int id;
    String name;
    String email;
    int courseCompletionPercentage;

    public User(int id, String name, String email, int courseCompletionPercentage) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.courseCompletionPercentage = courseCompletionPercentage;
    }
}
